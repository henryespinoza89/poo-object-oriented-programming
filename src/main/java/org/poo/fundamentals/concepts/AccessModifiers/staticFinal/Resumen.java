package org.poo.fundamentals.concepts.AccessModifiers.staticFinal;

public class Resumen {
  /* Resumen
   static:
   Variables: Son variables de clase compartidas por todas las instancias de la clase.
   Métodos: Son métodos de clase que pueden ser llamados sin crear una instancia de la clase.
   Bloques: Son bloques de inicialización que se ejecutan una vez cuando la clase se carga en memoria.
   final:
   Variables: Son constantes cuyo valor no puede cambiar una vez asignado.
   Métodos: No pueden ser sobrescritos por subclases.
   Clases: No pueden ser extendidas.
   Estos modificadores son esenciales para controlar el comportamiento, la accesibilidad y la inmovilidad de los
   componentes en un programa Java, promoviendo buenas prácticas de diseño y encapsulamiento.*/
}
