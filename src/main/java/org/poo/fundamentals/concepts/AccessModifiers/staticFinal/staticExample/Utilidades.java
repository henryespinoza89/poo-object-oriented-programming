package org.poo.fundamentals.concepts.AccessModifiers.staticFinal.staticExample;

public class Utilidades {
  /* Métodos static
   Definición: Un método static pertenece a la clase en lugar de a una instancia de la clase. Puede ser llamado
   sin crear una instancia de la clase.
   Acceso: Se puede acceder directamente usando el nombre de la clase.*/
  public static int sumar(int a, int b) {
    return a + b;
  }
}
